    private void atualizarTabela() {
        DefaultTableModel model = (DefaultTableModel) table_vendas.getModel();

        model.setRowCount(0); // limpa a tabela

        try {
            List<Venda> vendas = vendaController.findVendaEntities();

            for (Venda venda : vendas) {
                String linha[] = {
                    String.valueOf(venda.getIdVenda()),
                    venda.getDocCompraVenda(),
                    df.format(venda.getDataVenda()),
                    df.format(venda.getDataRetirada()),
                    venda.getFormaPagamento(),                   
                    venda.getFkVeiculos().toString(),
                    venda.getFkComprador().getCpf()
                };

                model.addRow(linha);
            }
            
            atualizarComboBox();
            
            if (comboBox_veiculo.getItemCount() > 0 && comboBox_cliente.getItemCount() > 0)
                button_inserir.setEnabled(true);
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                    this,
                    ex.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }
    
    private void atualizarComboBox() {
        List<Veiculos> veiculos = veiculoController.findVeiculosEntities();
        List<Cliente> clientes = clienteController.findClienteEntities();      

        DefaultComboBoxModel<Veiculos> veiculoModel = new DefaultComboBoxModel();       
        for (Veiculos veiculo : veiculos)
            veiculoModel.addElement(veiculo);
        comboBox_veiculo.setModel(veiculoModel);
        
        DefaultComboBoxModel<Cliente> clienteModel = new DefaultComboBoxModel(); 
        for (Cliente cliente : clientes)
            clienteModel.addElement(cliente);      
        comboBox_cliente.setModel(clienteModel);
    }
    
    private void limpar() {
        textField_idVenda.setText("");
        textField_documento.setText("");
        textField_dataVenda.setText("");
        textField_dataRetirada.setText("");
        comboBox_formaPagamento.setSelectedIndex(0);
        comboBox_veiculo.setSelectedIndex(0);
        comboBox_cliente.setSelectedIndex(0);

        atualizarTabela();

        button_inserir.setEnabled(true);
        button_remover.setEnabled(false);
        button_atualizar.setEnabled(false);
    }

