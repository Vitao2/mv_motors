    private void button_removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_removerActionPerformed
        try
        {
            int idRemover = Integer.parseInt(textField_idVenda.getText());
                     
            int confirm = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja remover o veículo ID: " + idRemover + "?", "Confirmar Remoção", JOptionPane.YES_NO_OPTION);
            
            //System.out.println("id que chegou: " + idRemover);
            
            if (confirm == JOptionPane.YES_OPTION) {
                // Passa o IdVeiculo (Integer) para o método destroy
                vendaController.destroy(idRemover);
                JOptionPane.showMessageDialog(this, "Veículo ID: " + idRemover + " removido com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                limpar();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                this,
                ex.getMessage(),
                "Erro",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_button_removerActionPerformed

    private void button_atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_atualizarActionPerformed
        try
        {
            if (vendaList.stream().anyMatch(v -> v.getText().equals("")))
            throw new Exception("Preencha todos os campos!");

            Venda venda = vendaController.findVenda(Integer.parseInt(textField_idVenda.getText()));

            venda.setDocCompraVenda(textField_documento.getText());
            venda.setDataVenda(df.parse(textField_dataVenda.getText()));
            venda.setDataRetirada(df.parse(textField_dataRetirada.getText()));
            venda.setFormaPagamento((String)comboBox_formaPagamento.getSelectedItem());
            venda.setFkVeiculos((Veiculos)comboBox_veiculo.getSelectedItem());
            venda.setFkComprador((Cliente)comboBox_cliente.getSelectedItem());

            vendaController.edit(venda);

            JOptionPane.showMessageDialog(
                this,
                "Produto: " + textField_idVenda.getText() + " alterado com sucesso!",
                "Sucesso",
                JOptionPane.INFORMATION_MESSAGE
            );

            limpar();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                this,
                ex.getMessage(),
                "Erro",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_button_atualizarActionPerformed

    private void table_vendasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_vendasMouseClicked
        DefaultTableModel model = (DefaultTableModel) table_vendas.getModel();
        
        if (evt.getClickCount() == 2)
        {
            int row = table_vendas.getSelectedRow();
            
            // Veiculo
            int veiculoId = Integer.parseInt(model.getValueAt(row, 5).toString().split(" \\| ")[0]);           
            Veiculos veiculo = veiculoController.findVeiculos(veiculoId); 
            
            // Cliente
            Cliente cliente = clienteController.findCliente(model.getValueAt(row, 6).toString());
            
            // atualizando tabela...
            textField_idVenda.setText(model.getValueAt(row, 0).toString());
            textField_documento.setText(model.getValueAt(row, 1).toString());
            textField_dataVenda.setText(model.getValueAt(row, 2).toString());
            textField_dataRetirada.setText(model.getValueAt(row, 3).toString());           
            comboBox_formaPagamento.setSelectedItem(model.getValueAt(row, 4).toString());            
            comboBox_veiculo.setSelectedItem(veiculo);
            comboBox_cliente.setSelectedItem(cliente);

            // buttons
            button_atualizar.setEnabled(true);
            button_remover.setEnabled(true);
            button_inserir.setEnabled(false);
        }
    }//GEN-LAST:event_table_vendasMouseClicked

