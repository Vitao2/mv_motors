    private void button_inserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_inserirActionPerformed

        try {
            if (vendaList.stream().anyMatch(v -> v.getText().equals("")))
            throw new Exception("Preencha todos os campos!");
            
            
            Veiculos newVeic = (Veiculos) comboBox_veiculo.getSelectedItem();
            Cliente newCliente = (Cliente) comboBox_cliente.getSelectedItem();

            Venda venda = new Venda(                               
                textField_documento.getText(),                            
                df.parse(textField_dataVenda.getText()),                             
                df.parse(textField_dataRetirada.getText()),                             
                (String)comboBox_formaPagamento.getSelectedItem(),
                newCliente,
                newVeic              
            );

            vendaController.create(venda);

            limpar();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                this,
                ex.getMessage(),
                "Erro",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_button_inserirActionPerformed

